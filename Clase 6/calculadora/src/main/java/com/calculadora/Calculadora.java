package com.calculadora;

public class Calculadora {
	
	public static Integer sumar(Integer numero1, Integer numero2) {
		return numero1 + numero2;
	}

	public static Integer multiplicar(Integer numero1, Integer numero2) {
		return numero1 * numero2;
	}
	
	public static Integer dividir(Integer numero1, Integer numero2) {
		return numero1 / numero2;
	}
	
	public static boolean esMayorOIgualQue(Integer numero1, Integer numero2) {
		return numero1 >= numero2;
	}
}
